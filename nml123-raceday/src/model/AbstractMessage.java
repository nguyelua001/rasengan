public abstract class AbstractMessage implements Message {
    /**
     * 
     */
    private final int myTimeStamp;
    /**
     * 
     */
    private final String myMessageType;

    /**
     * @param theTimeStamp timeStamp
     * @param theMessageType timeStamp
     */
    public AbstractMessage(final String theMessageType, final int theTimeStamp) {
        super();
        this.myTimeStamp = theTimeStamp;
        this.myMessageType = theMessageType;
    }

    @Override
    public String getMessageType() {
        // TODO Auto-generated method stub
        return myMessageType;
    }

    @Override
    public int getTimeStamp() {
        // TODO Auto-generated method stub
        return myTimeStamp;
    }

}